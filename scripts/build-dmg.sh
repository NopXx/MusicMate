# xcrun notarytool submit "$DMG_PATH" \
#     --keychain-profile "AC_PASSWORD" \
#     --wait
# xcrun stapler staple "$DMG_PATH"

# --- Done ------------------------------------------------------------------

log "Done"
ls -lh "$DMG_PATH"
echo
echo "Install: open '$DMG_PATH' → drag $APP_NAME to Applications."
